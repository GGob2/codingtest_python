# 리스트 원소의 합은 주어진 리스트의 모든 원소들의 합을 계산하는 알고리즘이니다.
# Python의 내장 함수 sum을 이용하여 리스트의 모든 원소의 합을 손쉽게 계산할 수 있습니다.

def list_element_sum(arr):
    return sum(arr)

# 예시 사용법
arr = [1, 2, 3, 4, 5]
print(list_element_sum(arr))  # 출력: 15
